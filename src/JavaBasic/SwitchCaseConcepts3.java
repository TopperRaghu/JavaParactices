package JavaBasic;

public class SwitchCaseConcepts3 {

	public static void main(String[] args) {
		int num = 10;
		switch (num) {
		case 10:
			System.out.println("PRINT NUM");
		//break;
		
		case 11:
			System.out.println("DON'T PRINT NUM");
		break;
		
		case 21:
			System.out.println("DON'T PRINT NUM");
		break;
		
		default:
			break;
		}
	}
	
	

}
