package JavaLoops;

public class TimeComplexity {

	public static void main(String[] args) {
		
//		00 01 02 03 04
//		10 11 12 13 14
//		20 21 22 23 24
//		30 31 32 33 34
//		40 41 42 42 44
		//nested loop
		//m*n matric
		for(int m=0; m<=4; m++) {
			for(int n=0; n<=5; n++) {
				System.out.print(m+""+n+" ");
			}
			System.out.println();
		}
		
		
		
		
		
		
	}
	

}
